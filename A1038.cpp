#include<bits/stdc++.h>
using namespace std;
const int maxn = 100010;
string num[maxn];
bool cmp(string a,string b){
	if(a+b<b+a) return true;
	else return false;
}
int main(){
    int n;
    scanf("%d",&n);
    for(int i=0;i<n;i++){
        cin>>num[i];
    }
    sort(num,num+n,cmp);
    for(int i=0;i<n;i++){
        if(i==0 && num[i][0]=='0'){
             while(num[i][0]=='0') num[i] = num[i].substr(1,num[i].size());
        }
        cout<<num[i];
       
    }
}

