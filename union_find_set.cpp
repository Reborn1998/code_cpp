#include<bits/stdc++.h>
using namespace std;
const int maxn=10010;
int father[maxn];
bool isroot[maxn];
int findFather(int x){
    int a = x;
    while(x!=father[x]){
        x = father[x];
    }
    while(a!=father[a]){
        int z = a;
        a = father[a];
        father[z] = x;
    }
    return x;

}
void init(int n){
    for(int i=1;i<=n;i++){
        father[i] = i;
        isroot[i] = false;
    }
}
void Union(int a,int b){
    int faa = findFather(a);
    int fab = findFather(b);
    if(faa!=fab){
        father[fab] = faa;
    }
}
int main() {
    int n,m;
    scanf("%d%d",&n,&m);
    init(n);
    for(int i=0;i<m;i++){
        int a,b;
        scanf("%d%d",&a,&b);
        Union(a,b);
    }
    int cnt = 0;
    for(int i=1;i<=n;i++){
        if(father[i]==i){
            cnt++;
        }
    }
    printf("%d\n",cnt);
}