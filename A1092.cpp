#include<bits/stdc++.h>
using namespace std;
int num[128] = {0};
int main(){
    string a,b;
    cin>>a>>b;
    for(int i=0;i<a.size();i++){
        num[a[i]]++;
    }
    for(int i=0;i<b.size();i++){
        num[b[i]]--;
    }
    int n=0,m=0;
    for(int i=0;i<128;i++){
        if(num[i]>0){
            n+=num[i];
        }
        else{
            m+=num[i];
        }
    }
    if(m!=0){
        cout<<"No "<<-m;
    }
    else{
        cout<<"Yes "<<n;
    }
}