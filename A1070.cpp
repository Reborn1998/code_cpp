#include<bits/stdc++.h>
using namespace std;
struct Moomcake{
    double amount;
    double price;
}moom[1010];
bool cmp(Moomcake a,Moomcake b){
    return a.price/a.amount>b.price/b.amount;
}
// 注意存货和价格均为浮点数
int main(){
    int n;
    double d;
    scanf("%d%lf",&n,&d);
    for(int i=0;i<n;i++){
        scanf("%lf",&moom[i].amount);
    }
    for(int i=0;i<n;i++){
        scanf("%lf",&moom[i].price);
    }
    sort(moom,moom+n,cmp);
    int idx=0;
    double sum=0;
    while(d!=0 && idx<n){
        if(d>=moom[idx].amount){
            sum += moom[idx].price;
            d -= moom[idx].amount;
        }
        else if(d<moom[idx].amount){
            sum+= moom[idx].price*d/moom[idx].amount;
            d=0;
        }
        idx++;
    }
    printf("%.2f",sum);
}