#include<bits/stdc++.h>
using namespace std;
vector<int> nc,np;
int n,m;
bool cmp(int a,int b){
    if(a<0 && b<0) return a<b;
    return a>b;
}
int main(){
    int temp;
    scanf("%d",&n);
    for(int i=0;i<n;i++){
        scanf("%d",&temp);
        nc.push_back(temp);
    }
    scanf("%d",&m);
    for(int i=0;i<m;i++){
        scanf("%d",&temp);
        np.push_back(temp);
    }

    sort(nc.begin(),nc.end(),cmp);
    sort(np.begin(),np.end(),cmp);
    int sum=0;
    int i=0,idx=0;
  
    while(true){
        
        if(np[i]*nc[idx]>0){
            sum += np[i++]*nc[idx++];
            
        }else{
            while(np[i]>=0&&i<m){++i;}
            while(nc[idx]>=0&&idx<n){idx++;}
        }
        if(i==m || idx==n) break;
    }
    printf("%d",sum);
}


