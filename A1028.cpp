#include<bits/stdc++.h>
using namespace std;
struct Record{
    int id;
    char name[10];
    int score;
}stu[100010];
bool cmp0(Record a,Record b){
    return a.id<b.id;
}
bool cmp1(Record a,Record b){
    int s  = strcmp(a.name,b.name);
    if(s!=0)
        return s<0;
    return a.id<b.id;
}
bool cmp2(Record a,Record b){
    if(a.score!=b.score)
        return a.score<b.score;
    return a.id<b.id;
}
int main(){
    int n,c;
    scanf("%d%d",&n,&c);
    for(int i=0;i<n;i++){
        scanf("%d%s%d",&stu[i].id,stu[i].name,&stu[i].score);
    }
    if(c==1){
        sort(stu,stu+n,cmp0);
    }else if(c==2){
        sort(stu,stu+n,cmp1);
    }if(c==3){
        sort(stu,stu+n,cmp2);
    }
    for(int i=0;i<n;i++){
        printf("%06d %s %d\n",stu[i].id,stu[i].name,stu[i].score);
    }
}