#include<bits/stdc++.h>
using namespace std;
const int maxn=100010;
struct Record{
    char name[11];
    char id[11];
    int score;
}stu[maxn];

bool cmp(Record a,Record b) {
    return a.score>b.score;
}

int main(){
    int n;
    scanf("%d",&n);
    for(int i=0;i<n;i++) {
        scanf("%s %s %d",stu[i].name,stu[i].id,&stu[i].score);
    }
    int gradeL,gradeR;
    scanf("%d%d",&gradeL,&gradeR);
    int cnt = 0;
    sort(stu,stu+n,cmp);
    for(int i=0;i<n;i++){
        if(stu[i].score>=gradeL && stu[i].score<=gradeR) {
            printf("%s %s\n",stu[i].name,stu[i].id);
            cnt++;
        }
    }
    if(cnt==0){
        printf("NONE\n");
    }
}