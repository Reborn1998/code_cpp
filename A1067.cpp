#include<bits/stdc++.h>
using namespace std;
const int maxn = 100010;
int num[maxn];
int main(){
    int n;
    scanf("%d",&n);
    int idx0=0;
    int left=0;
    for(int i=0;i<n;i++){
        scanf("%d",&num[i]);
        if(num[i]==0) idx0=i;
        if(num[i]!=i) left++;
    }
    int cnt=0;
    int k=n;
    int K=1;
    while(left!=0){
    	while(num[k-1]==k-1)k--;
        while(num[K]==K)K++;
    	if(k==0)break;
        if(idx0==0){
            for(int i=0;i<k;i++){
                if(i==1)i=K;
                if(num[i]!=i){
                    swap(num[i],num[idx0]);
//                    for(int j=0;j<n;j++){
//                    	cout<<num[j]<<" ";
//					}
// 					cout<<endl;
                    cnt++;
                    idx0=i;
                    break;
                }
            }
        }
        for(int i=0;i<k;i++){
            if(i==1)i=K;
//        	cout<<"?";
        	if(idx0==0) break;
        	
            if(num[i]==idx0){
				
                swap(num[i],num[idx0]);
//                for(int j=0;j<n;j++){
//                    	cout<<num[j]<<" ";
//				}
//				cout<<"ss"<<endl;
                cnt++;
                left--; 
                idx0=i;
                break;
            }
        }
//        cout<<k<<"ooo\n";
    }
    printf("%d",cnt);

}


