using namespace std;
int main(){
    int num[1005]={0};
    int n,m,a;
    scanf("%d%d",&n,&m);
    for(int i=0;i<n;i++){
        scanf("%d",&a);
        if(a<1002) num[a]++;
    }
    bool flag = false;
    for(int i=0;i<=m/2;i++){
        if(num[i] && num[m-i]){
            if(i==m-i && num[i]<2) break; 
            printf("%d %d",i,m-i);
            flag = true;
            break;
        }
    }
    if(!flag){
        printf("No Solution");
    }
}