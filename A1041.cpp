#include<bits/stdc++.h>
using namespace std;
const int maxn = 10010;
queue<int> q;
int num[maxn] = {0};
int main(){
    int n;
    scanf("%d",&n);

    int a;
    for(auto i=0;i<n;i++){
        scanf("%d",&a);
        num[a] += 1;
        q.push(a);
    }
    while(!q.empty()){
        a = q.front();
        q.pop();
        if(num[a]==1){
            printf("%d",a);
            break;
        }
    }
    if(num[a]!=1){
        printf("None");
    }
    
    
}